public class EgetException extends Exception {

    /**
     *
     */
    private static final long serialVersionUID = 1L;
    
    public EgetException() {
        super();
    }

    public EgetException(String message) {
        super(message);
    }

    public EgetException(String message, Throwable cause) {
        super(message, cause);
    }

    public EgetException(Throwable cause) {
        super(cause);
    }
}
